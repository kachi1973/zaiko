		private static int get_codes_id(NpgsqlConnection conn, int type_id, int parent_id, string name)
		{
			using (NpgsqlCommand cmd = new NpgsqlCommand(@"select id, parent_id, name from codes where (type_id = :type_id) and (parent_id = :parent_id) and (name = :name)", conn))
			{
				NpgsqlParameter prm;
				prm = cmd.Parameters.Add(new NpgsqlParameter("type_id", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = type_id;
				prm = cmd.Parameters.Add(new NpgsqlParameter("parent_id", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = parent_id;
				prm = cmd.Parameters.Add(new NpgsqlParameter("name", NpgsqlTypes.NpgsqlDbType.Text));
				prm.Value = name;
				var rdr = cmd.ExecuteReader();
				while (rdr.Read())
				{
					var rdr_id = rdr["id"];
					var rdr_parent_id = rdr["parent_id"];
					var rdr_name = rdr["name"];
					rdr.Close();
					return (int)rdr_id;
				}
				rdr.Close();
				return 0;
			}
		}
		private static int insert_codes(NpgsqlConnection conn, int type_id, int parent_id, string name, int type2_id, int level)
		{
			using (var cmd = new NpgsqlCommand(@"insert into codes(type_id, parent_id, name, type2_id, level) values(:type_id, :parent_id, :name, :type2_id, :level) RETURNING id;", conn))
			{
				log(String.Format("コード追加：{0}", name));
				NpgsqlParameter prm;
				prm = cmd.Parameters.Add(new NpgsqlParameter("type_id", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = type_id;
				prm = cmd.Parameters.Add(new NpgsqlParameter("parent_id", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = parent_id;
				prm = cmd.Parameters.Add(new NpgsqlParameter("name", NpgsqlTypes.NpgsqlDbType.Text));
				prm.Value = name;
				prm = cmd.Parameters.Add(new NpgsqlParameter("type2_id", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = type2_id;
				prm = cmd.Parameters.Add(new NpgsqlParameter("level", NpgsqlTypes.NpgsqlDbType.Integer));
				prm.Value = level;
				return (int)cmd.ExecuteScalar();
			}
		}
