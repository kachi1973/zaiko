		public static int insert_codes_id(NpgsqlConnection conn, int type_id, int parent_id, string name)
		{
			name = name.Trim();
			name = name.Replace("\n", "");
			name = name.Replace("\r", "");
			switch (type_id)
			{
			case 2:
				if (Lib.makers.ContainsKey(name))
				{
					name = Lib.makers[name];
				}
				break;
			case 3:
				if (Lib.kikis.ContainsKey(name))
				{
					name = Lib.kikis[name];
				}
				break;
			}
			int id = 0;
			if (0 < name.Length)
			{
				id = get_codes_id(conn, type_id, parent_id, name);
				if (id == 0)
				{
					id = insert_codes(conn, type_id, parent_id, name, 0, 0);
				}
			}
			return id;
		}
